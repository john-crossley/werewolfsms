<p>A fearless individual determined to protect the innocent, even at the expense of himself. At night, the Huntsman can guard a player of their choosing from the werewolves. If that person is attacked, the Huntsman and a randomly select werewolf will both be killed.</p>

<h2>The Gravedigger</h2>

<p>The Gravedigger is able to check dead players at night. He can learn the exact role that player had.</p>

<h2>Lycans</h2>

<p>Lycans are unfortunate villagers afflicted with a mild form of Lycanthropy. They're not evil, and are on the side of the village. Unforunately however, the Seer will view any such individuals as werewolves at night. The Lycans therefore, despite being part of the village, are a burden on it.</p>

<h2>The Werewolves</h2>

<p>The werewolves represent the informed minority. It is their goal to remain undetected long enough to kill every villager. 
    Each werewolf is aware who the wolves are and can communicate in secret.</p>
    
<h2>The Bloodhound</h2>

<p>Bloodhounds spend their night sniffing out the werewolves prey. Once per night, they can learn the role of a member of a village, directing the wolves to more important targets like the Seer.</p>
    
<h2>The Shapeshifter</h2>

<p>The Shapeshifter is a powerful werewolf, able to turn the tide of an otherwise hopelessly lost game. The Shapeshifter has a one time ability to swap identities with a victim of the werewolves. The rest of the game will see that the Shapeshifter has died, but 
in truth, he has now assumed the identity of the dead villager.</p>

