<h2>The Village</h2>

<p>The village represent the uninformed majority. None of them know anyone's role apart from their own. Most players will take on the role of 
a villager, unsure of whom they can trust as they work out who the werewolves are.</p>

<h2>The Seer</h2>

<p>The Seer is a special role who is part of the village. Once per night, they can pick a player to learn whether they
are an innocent villager, or an evil werewolf. The seer must be careful however, should they reveal their role the werewolves will surely kill them the next night.</p>

<h2>The Protector</h2>

<p>A guardian angel of sorts, once per night the Protector can shield a player from harm. Although the Protector cannot choose the same person to guard two consecutive nights.</p>

<h2>The Huntsman</h2>
